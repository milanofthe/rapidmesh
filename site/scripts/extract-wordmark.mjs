// Offline wordmark extraction: bake the "RapidMESH" outline from JetBrains Mono
// SemiBold (weight 600 -- the rapidpassives family brand weight) into
// src/lib/wordmark.ts -- both the flattened polygon loops (the holes the WASM
// mesher triangulates around) and the SVG path (rendered crisp as the accent
// wordmark). Run once; re-run if the text or font changes:
//     cd site && node scripts/extract-wordmark.mjs
import opentype from 'opentype.js';
import { readFileSync, writeFileSync } from 'node:fs';

const TEXT = 'RapidMESH';
const UPM = 1000; // reference units: baseline y=0, x starts at 0, font size 1000
const N = 12; // bezier subdivision per curve segment

const buf = readFileSync('static/fonts/JetBrainsMono-SemiBold.ttf');
const font = opentype.parse(buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength));
const path = font.getPath(TEXT, 0, 0, UPM);

const loops = [];
let cur = [];
let px = 0;
let py = 0;
const cubic = (x1, y1, x2, y2, x3, y3) => {
	for (let t = 1; t <= N; t++) {
		const u = t / N;
		const v = 1 - u;
		const a = v * v * v;
		const b = 3 * v * v * u;
		const c = 3 * v * u * u;
		const d = u * u * u;
		cur.push(a * px + b * x1 + c * x2 + d * x3, a * py + b * y1 + c * y2 + d * y3);
	}
};
const quad = (x1, y1, x2, y2) => {
	for (let t = 1; t <= N; t++) {
		const u = t / N;
		const v = 1 - u;
		const a = v * v;
		const b = 2 * v * u;
		const c = u * u;
		cur.push(a * px + b * x1 + c * x2, a * py + b * y1 + c * y2);
	}
};
for (const c of path.commands) {
	if (c.type === 'M') {
		if (cur.length) loops.push(cur);
		cur = [c.x, c.y];
		px = c.x;
		py = c.y;
	} else if (c.type === 'L') {
		cur.push(c.x, c.y);
		px = c.x;
		py = c.y;
	} else if (c.type === 'C') {
		cubic(c.x1, c.y1, c.x2, c.y2, c.x, c.y);
		px = c.x;
		py = c.y;
	} else if (c.type === 'Q') {
		quad(c.x1, c.y1, c.x, c.y);
		px = c.x;
		py = c.y;
	}
}
if (cur.length) loops.push(cur);

// Douglas-Peucker on each contour: the N=12 bezier flattening is far denser
// than the wordmark needs; thinning it (tol in upm) keeps the shape crisp while
// cutting the hole/segment count the mesher carries.
const TOL = 9;
const pdist = (p, a, b) => {
	const dx = b[0] - a[0];
	const dy = b[1] - a[1];
	const l2 = dx * dx + dy * dy;
	if (l2 === 0) return Math.hypot(p[0] - a[0], p[1] - a[1]);
	const t = ((p[0] - a[0]) * dx + (p[1] - a[1]) * dy) / l2;
	return Math.hypot(p[0] - (a[0] + t * dx), p[1] - (a[1] + t * dy));
};
const simplify = (flat, tol) => {
	const pts = [];
	for (let i = 0; i < flat.length; i += 2) pts.push([flat[i], flat[i + 1]]);
	const n = pts.length;
	if (n < 4) return flat;
	const keep = new Array(n).fill(false);
	keep[0] = keep[n - 1] = true;
	const stack = [[0, n - 1]];
	while (stack.length) {
		const [a, b] = stack.pop();
		let md = -1;
		let mi = -1;
		for (let i = a + 1; i < b; i++) {
			const d = pdist(pts[i], pts[a], pts[b]);
			if (d > md) {
				md = d;
				mi = i;
			}
		}
		if (md > tol) {
			keep[mi] = true;
			stack.push([a, mi], [mi, b]);
		}
	}
	return pts.filter((_, i) => keep[i]).flat();
};

const round = (v) => Math.round(v * 100) / 100;
const flatLoops = loops
	.filter((lp) => lp.length / 2 >= 3)
	.map((lp) => simplify(lp, TOL))
	.filter((lp) => lp.length / 2 >= 3)
	.map((lp) => lp.map(round));
const bb = path.getBoundingBox();
const d = path.toPathData(2);

const out = `// AUTO-GENERATED by scripts/extract-wordmark.mjs -- do not edit by hand.
// "${TEXT}" in JetBrains Mono SemiBold, outline at ${UPM} upm (baseline y=0, x from 0).
// \`loops\` are the flattened polygon contours the WASM mesher holes out;
// \`path\` is the crisp SVG path drawn as the accent wordmark. Same coordinate
// system -- scale + translate both together to place in the viewport.
export const WORDMARK = {
	text: ${JSON.stringify(TEXT)},
	upm: ${UPM},
	bbox: { x1: ${round(bb.x1)}, y1: ${round(bb.y1)}, x2: ${round(bb.x2)}, y2: ${round(bb.y2)} },
	path: ${JSON.stringify(d)},
	loops: ${JSON.stringify(flatLoops)}
} as const;
`;
writeFileSync('src/lib/wordmark.ts', out);
const verts = flatLoops.reduce((a, l) => a + l.length / 2, 0);
console.log(`wrote src/lib/wordmark.ts: ${flatLoops.length} loops, ${verts} verts, bbox`, bb);
